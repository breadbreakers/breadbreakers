                management committee and all volunteers, regardless of the
                nature or duration of their involvement.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                When and How to Declare
            </h3>
            <p>
                A declaration must be made as soon as an individual becomes
                aware of any actual, perceived, or potential conflict of
                interest.
            </p>
            <p>
                Declarations should be submitted to the President in writing.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                What to Declare
            </h3>
            <p>
                Members and volunteers should disclose any situation where
                personal interests could conflict with their responsibilities to
                Bread Breakers Singapore. Common examples include:
            </p>
            <ul>
                <li>
                    Relationships with vendors, donors, or partner organisations
                    that may influence decision making
                </li>
                <li>
                    Financial interests in organisations that may benefit from a
                    relationship with Bread Breakers Singapore
                </li>
                <li>
                    Gifts or benefits received in connection with Bread Breakers
                    Singapore roles
                </li>
            </ul>
            <p>
                These examples are not exhaustive. When in doubt, individuals
                are encouraged to seek guidance from the President,
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                Non-Compliance
            </h3>
            <p>
                Failure to declare a conflict of interest, whether intentional or
                due to oversight, may result in the termination of partnership, as determined by the management committee.
            </p>
