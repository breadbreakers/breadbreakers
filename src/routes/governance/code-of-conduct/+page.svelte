<section class="section">
    <div class="container">

        <h2 class="subtitle is-4 has-text-weight-bold pt-4">Code of Conduct</h2>

        <div class="content">
            <h3 class="subtitle is-5 mt-5 has-text-weight-bold">Purpose</h3>
            <p>
                This Code of Conduct outlines the expected standards of behavior
                for all volunteers engaged with Bread Breakers (SG). It ensures a
                safe, respectful, and professional environment for volunteers,
                beneficiaries, and the broader community.
            </p>

            <p>
                Volunteers are expected to uphold the highest standards of
                conduct in all their roles and interactions. "Volunteer" refers
                to any person offering services without compensation to support the
                Bread Breakers (SG) mission.
            </p>

            <h3 class="subtitle is-5 mt-5 has-text-weight-bold">Scope</h3>
            <p>
                This Code applies to all individuals who voluntarily offer their
                time and services to Bread Breakers (SG) without payment or material
                benefit. It governs conduct during volunteer activities, as well
                as any interactions involving the organization, its
                stakeholders, and the public.
            </p>

            <p>
                "Confidential Information" includes, but is not limited to,
                personal data, and any information related to beneficiaries,
                donors, or gifts. "Personal Data" shall follow the definition
                set out in the Personal Data Protection Act 2012 (PDPA),
                including photographs, images, and videos. Volunteers must
                obtain consent before capturing any such data.
            </p>

            <h3 class="subtitle is-5 mt-5 has-text-weight-bold">Responsibilities</h3>
            <p>Volunteers are expected to:</p>
            <ul>
                <li>
                    Use resources and information solely for
                    authorised purposes related to volunteer duties.
                </li>
                <li>
                    Perform duties in good faith, responsibly, safely, and with
                    care, in the best interests of the organisation and its
                    beneficiaries.
                </li>
                <li>
                    Communicate respectfully and professionally with all
                    individuals.
                </li>
                <li>
                    Maintain the confidentiality of all data and information
                    obtained during volunteering.
                </li>
                <li>
                    Notify the President should they choose to end their volunteer
                    relationship.
                </li>
            </ul>

            <h3 class="subtitle is-5 mt-5 has-text-weight-bold">Prohibited Conduct</h3>
            <p>Volunteers are not to:</p>
            <ul>
                <li>
                    Engage in actions that could bring harm or disrepute to
                    the Bread Breakers (SG) or its beneficiaries.
                </li>
                <li>
                    Seek or accept personal rewards, gifts, or benefits.
                </li>
                <li>
                    Participate in activities while under the influence of
                    alcohol or non-prescription drugs.
                </li>
                <li>Falsify or alter any records or documents.</li>
                <li>
                    Share photographs or videos of beneficaries or their living arragements on social media platforms without
                    prior consent from the beneficary and President.
                </li>
                <li>
                    Act as a spokesperson without prior written
                    authorisation.
                </li>
            </ul>

            <h3 class="subtitle is-5 mt-5 has-text-weight-bold">Confidentiality and Data Protection</h3>
            <ul>
                <li>
                    Confidential Information must be used only for purposes
                    necessary to fulfill volunteer duties.
                </li>
                <li>
                    Such information must not be disclosed to third parties,
                </li>
                <li>
                    Personal Data must not be copied or downloaded onto personal devices.
                </li>
                <li>
                    Volunteers must comply with the PDPA, the Charities Act
                    (Cap. 37), and related legislation regarding personal and
                    donor data.
                </li>
            </ul>
