            <p>Exceptions will be permitted only upon documented request by the social worker or beneficiary, or in exceptional cases where the retailer offers a substantial discount, subject to the approval of the President.</p>

            <p>
                No physical (in-store) purchases are allowed. This restriction
                helps mitigate the submission of fraudulent
                receipts and ensures all transactions are tagged to individuals requesting for claims.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                Procurement Process
            </h3>

            <p>
                Procurement can only take place after a social worker has
                confirmed the arrangement to deliver goods and provided the
                recipient‚Äôs delivery address.
            </p>

            <p>
                All purchases, regardless of amount, must be approved by the
                President of the organisation before proceeding.
            </p>

            <p>
                Procurement decisions will be based on an optimal balance of
                benefits and costs, considering:
            </p>
            <ul>
                <li>Quality and suitability for the intended purpose</li>
                <li>Reliability of the product and supplier</li>
                <li>
                    Risk (including delivery timelines and after-sales support)
                </li>
                <li>Price (not solely the lowest price)</li>
            </ul>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                Purchase and Documentation
            </h3>

            <p>
                Proof of purchase must be submitted in the form of a screenshot
                or PDF copy of the email receipt and must contain the billed
                name and address.
            </p>
            <p>
                All receipts will be stored securely by Bread Breakers (SG) for audit
                purposes.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                Segregation of Duties
            </h3>

            <p>
                The social worker must initiate the request and provide delivery
                details of the beneficary.
            </p>
            <p>
                Volunteers must strictly follow the instructions and item lists
                provided by the assigned social worker. No additional items,
                gifts, or monetary contributions may be included or offered
                beyond what has been approved. This restriction is essential to
                ensure consistency, fairness, and compliance with the broader
                government support system. Providing unsolicited or excess items
                may unintentionally jeopardize the recipient‚Äôs eligibility for
                government assistance or lead to undue scrutiny regarding the
                origin of the goods or perceived income.
            </p>

            <p>
                The President or appointed delegate shall review and approve all procurement requests.
            </p>

            <p>
                Due to our lean, volunteer-led model, individuals serving on the
                Working Group/Committee may also directly serve as Partners
                involved in aid procurement and delivery. To maintain strict
                integrity and transparency, a Working Group member acting as a
                Partner cannot approve their requests, and must be approved by
                another designated Committee member.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                 Ethical Standards &amp; Conflict of Interest
            </h3>

            <p>
                All individuals involved in procurement must act with integrity
                and avoid any actual or perceived conflicts of interest.
            </p>

            <h3 class="subtitle is-6 mt-5 has-text-weight-bold">
                 Record Keeping
            </h3>

            <p>
                All procurement records, including requests, approvals,
                receipts, and delivery confirmations, must be retained for a
                minimum of 5 years in accordance with Singapore's
                regulatory requirements for audit and accountability purposes.
            </p>
